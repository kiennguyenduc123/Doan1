    private void tableNCCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableNCCMouseClicked
        // TODO add your handling code here:
      int selectedRow = tableNCC.getSelectedRow();

    if (selectedRow >= 0) {
        String maNCC = tableNCC.getValueAt(selectedRow, 0).toString();
        String tenNCC = tableNCC.getValueAt(selectedRow, 1).toString();
        String sdt = tableNCC.getValueAt(selectedRow, 2).toString();
        String diaChi = tableNCC.getValueAt(selectedRow, 3).toString();
        String trangThai = tableNCC.getValueAt(selectedRow, 4).toString().trim();

        // Set lên các trường nhập liệu
        txtMancc.setText(maNCC);
        txtTenncc.setText(tenNCC);
        txtSdt.setText(sdt);
        txtDiachi.setText(diaChi);

        // Ghi log để kiểm tra trạng thái
        System.out.println("➡️ Trạng thái lấy được từ bảng: '" + trangThai + "'");

        // Xử lý chọn RadioButton dựa trên trạng thái
        if (trangThai.equalsIgnoreCase("Đang hợp tác")) {
            rdoDang.setSelected(true);
        } else if (trangThai.equalsIgnoreCase("Ngừng hợp tác")) {
            rdoNgung.setSelected(true);
        } else {
            // Nếu trạng thái không khớp, bỏ chọn cả hai (phòng lỗi)
            btnGroupTrangThai.clearSelection(); // ← YÊU CẦU đã khai báo ButtonGroup trước đó
        }
    }
    }//GEN-LAST:event_tableNCCMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String maNCC = txtMancc.getText().trim();
        String tenNCC = txtTenncc.getText().trim();
        String sdt = txtSdt.getText().trim();
        String diaChi = txtDiachi.getText().trim();
        boolean dangHopTac = rdoDang.isSelected();

        if (maNCC.isEmpty() || tenNCC.isEmpty() || sdt.isEmpty() || diaChi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return;
        }

        String trangThai = dangHopTac ? "Đang hợp tác" : "Ngừng hợp tác";

        // Tạo đối tượng NCC
        NhaCC ncc = new NhaCC();
        ncc.setMancc(maNCC);
        ncc.setTenncc(tenNCC);
        ncc.setSdt(sdt);
        ncc.setDiachi(diaChi);
        ncc.setTrangthai(trangThai);

        // Gọi DAO để thêm vào DB
        boolean themThanhCong = dao.insert(ncc); // Giả sử bạn có nccDAO

        if (themThanhCong) {
            JOptionPane.showMessageDialog(this, "Thêm nhà cung cấp thành công!");
            loadNhacc(); // Load lại bảng
            txtMancc.setText("");
            txtTenncc.setText("");
            txtSdt.setText("");
            txtDiachi.setText("");
            btnGroupTrangThai.clearSelection(); // ← YÊU CẦU đã khai báo ButtonGroup trước đó
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại! Mã có thể đã tồn tại.");
        } 
    }//GEN-LAST:event_btnThemActionPerformed

