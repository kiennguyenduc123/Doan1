    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableNCC.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một nhà cung cấp để cập nhật.");
            return;
        }

        String maNCC = txtMancc.getText().trim();
        String tenNCC = txtTenncc.getText().trim();
        String sdt = txtSdt.getText().trim();
        String diaChi = txtDiachi.getText().trim();
        boolean dangHopTac = rdoDang.isSelected();

        if (maNCC.isEmpty() || tenNCC.isEmpty() || sdt.isEmpty() || diaChi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống thông tin!");
            return;
        }

        String trangThai = dangHopTac ? "Đang hợp tác" : "Ngừng hợp tác";

        // Tạo đối tượng NCC
        NhaCC ncc = new NhaCC();

        ncc.setMancc(maNCC);
        ncc.setTenncc(tenNCC);
        ncc.setSdt(sdt);
        ncc.setDiachi(diaChi);
        ncc.setTrangthai(trangThai);
        
        boolean capNhatThanhCong = dao.update(ncc); // Gọi DAO cập nhật

        if (capNhatThanhCong) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            loadNhacc(); // Load lại bảng
            txtMancc.setText("");
            txtTenncc.setText("");
            txtSdt.setText("");
            txtDiachi.setText("");
            btnGroupTrangThai.clearSelection(); // ← YÊU CẦU đã khai báo ButtonGroup trước đó
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed


