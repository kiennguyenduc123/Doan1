    private void loadNhacc(){
        tableModel.setRowCount(0);
        nhaccList = dao.getAllNhaCC();
        for(NhaCC a : nhaccList) {
            tableModel.addRow(new Object[] {
                a.getMancc(),
                a.getTenncc(),
                a.getSdt(),
                a.getDiachi(),
                a.getTrangthai()
            });
        }
    }

