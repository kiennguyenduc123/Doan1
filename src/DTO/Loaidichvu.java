package DTO;

/**
 *
 * @author Kiennguyen
 */
public class Loaidichvu {
    public int id;  
    public String maloai, tenloai;
    
    public Loaidichvu() {}

    public Loaidichvu(int id, String maloai, String tenloai) {
        this.id = id;
        this.maloai = maloai;
        this.tenloai = tenloai;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMaloai() {
        return maloai;
    }

    public void setMaloai(String maloai) {
        this.maloai = maloai;
    }

    public String getTenloai() {
        return tenloai;
    }

    public void setTenloai(String tenloai) {
        this.tenloai = tenloai;
    }
    
    
}
